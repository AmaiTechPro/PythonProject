#Polymorphism  -Many forms of method/Same method different implementation for all the objects

#CREATING CLASSES
class Dog:
    #Behaviour
    def sound(self):
        print("Woof!! Woof!!")


class Cat:
    #Behaviour
    def sound(self):
        print("Meow!! Meow!!")

#CREATING OBJECTS
d = Dog()
d.sound()

c = Cat()
c.sound()


































































































