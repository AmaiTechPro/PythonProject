# 4. Logical Operators  - Used where we have more than one condition to be executed at same time. They include: and, or, not

#N/B: And returns a true only if all conditions mentioned are true
#N/B: Or returns a true if any of the condition mentioned is true
#N/B: Not reverses a result i.e if the answer was true, it returns false
     
            #Examples;

print(50>16 and 67>20 and 45<30)          

print(50>16 or 67>20 or 45<30)

print(not(50>16 and  67>20 and  45<30))

print(not(19!=20))




