# 1. Arithmetic Operators -Used to perform simple calculationes include: addition(+),subtraction (-), multiplication(*), division(/), modulus(%).


x = 34

y = 2

print(x+y)

print(x-y)

print(x/y)

print(x*y)

print(x%y) #Modulus - Return the remainder after division




# 2. Comparison Operators - Used to compare values against each other and give either True or False since its a boolean

z = 60
w = 68


print(z>w)

print(z!=w)

print(w>=z)

print(z!=w)    #Not equal to comparison operator

print(z<w)

print(z<=w)

print(z==w)  #Equal to comparison operator

print(z==60)




